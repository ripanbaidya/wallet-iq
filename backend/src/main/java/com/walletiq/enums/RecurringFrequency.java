package com.walletiq.enums;

/**
 * Defines how often a recurring transaction repeats.
 */
public enum RecurringFrequency {

    /**
     * Occurs every day
     */
    DAILY,

    /**
     * Occurs once every week
     */
    WEEKLY,

    /**
     * Occurs once every month
     */
    MONTHLY,

    /**
     * Occurs once every year
     */
    YEARLY
}